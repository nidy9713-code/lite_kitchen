async def run_import(notify=False):
    bot = Bot(token=TOKEN) if notify else None
    
    conn = sqlite3.connect('recipes.db')
    cursor = conn.cursor()
    
    # Get existing titles to avoid simple duplicates
    cursor.execute("SELECT title FROM recipes")
    existing_titles = {row[0] for row in cursor.fetchall()}
    conn.close()

    print(f"Начинаю импорт {len(all_recipes)} рецептов...")
    count = 0
    for r in all_recipes:
        if r['title'] in existing_titles:
            continue
            
        data = r.copy()
        data['photo_id'] = None
        
        # Clean ingredients
        data['ingredients'] = data['ingredients'].replace("/", "\n— ")
        if not data['ingredients'].startswith("— "):
            data['ingredients'] = "— " + data['ingredients']
        
        # Clean steps (add newlines between numbers if they are in one line)
        import re
        data['steps'] = re.sub(r'(\d+\.)', r'\n\1', data['steps']).strip()
            
        await db.add_recipe(data, bot=bot)
        count += 1
    
    if bot:
        await bot.session.close()
        
    print(f"Импорт завершен! Добавлено новых рецептов: {count}")

if __name__ == "__main__":
    # Установите notify=True, если хотите, чтобы при импорте ушли уведомления пользователям
    asyncio.run(run_import(notify=False))
