async def import_smoothies(notify=False):
    bot = [Bot(token=t) for t in TOKENS] if notify else None
    
    # Сначала удалим старые смузи из этой категории, если они есть, чтобы не дублировать
    # Или просто проверим по названиям
    from bot.database.db import db
    
    # Получаем существующие названия
    existing_recipes = await db.get_recipes_by_category("Смузи для детей")
    existing_titles = {r['title'] for r in existing_recipes}
    
    count = 0
    updated_count = 0
    for s in SMOOTHIES:
        if s['title'] in existing_titles:
            # Если уже есть, обновим данные
            for r in existing_recipes:
                if r['title'] == s['title']:
                    recipe_id = r['id']
                    # Важно: tags в r уже строка JSON, нужно распарсить перед передачей в update_recipe,
                    # так как update_recipe сам делает json.dumps
                    try:
                        current_tags = json.loads(r['tags']) if r['tags'] else []
                    except:
                        current_tags = []
                        
                    # Обновляем cook_time, time_category и meal_type
                    updated_data = {
                        **r, 
                        "cook_time": 7, 
                        "time_category": "quick", 
                        "meal_type": "Напиток",
                        "tags": current_tags # передаем как список
                    }
                    await db.update_recipe(recipe_id, updated_data)
                    updated_count += 1
                    break
            continue
            
        await db.add_recipe(s, bot=None)
        count += 1
        
    print(f"Обновлено: {updated_count}, Добавлено: {count}")
    
    if (count > 0 or updated_count > 0) and notify and bot:
        user_ids = await db.get_all_user_ids()
        if count > 0:
            text = (
                f"🔔 <b>У нас обновление!</b>\n\n"
                f"🥤 В раздел <b>Смузи для детей</b> добавлено {count} новых рецептов! ✨\n\n"
                f"Загляните в бота, чтобы попробовать новинки! 😋"
            )
        else:
            text = (
                f"🔔 <b>Обновление рецептов!</b>\n\n"
                f"🥤 Мы обновили информацию в разделе <b>Смузи для детей</b>. Теперь все рецепты стали еще удобнее! ✨\n\n"
                f"Загляните в бота, чтобы посмотреть! 😋"
            )
        
        for uid in user_ids:
            try:
                await bot.send_message(uid, text, parse_mode="HTML", protect_content=True)
                await asyncio.sleep(0.05)
            except:
                continue
        
    if bot:
        await bot.session.close()
        
    print(f"Импортировано {count} новых смузи.")

if __name__ == "__main__":
    # Установите notify=True, если хотите разослать уведомления
    asyncio.run(import_smoothies(notify=True))
